-- The following stages may consist of: startup, shutdown, tick.
-- Tick will take place as often as scripted zone events are updated, while the stage is active.

-- Setup Stage ----------------------------------------------------------------
setupStage = Stage:new{ name = "Setup" }
setupStage.startup = function(self)
	Message.DebugPrint("Starting Stage Setup for Deathsurge")

--  Lines 16 to 18 is doing the following in a more efficient manner:
--	SZEMain:addTeam("LongbowStage1", SZETeam:new{ name = "LongbowStage1", layout = "LongbowStage1", killBehavior = "DestroyMe"} )

	teamNames = {"Deathsurge", "Fliers"}
	for i, v in ipairs(teamNames) do
		SZEMain:addTeam(v, SZETeam:new{ name = v, layout = v, killBehavior = "DestroyMe"} )
	end

	SZEMain:addCounter("Deathsurge", Counter:new{ ctype = "Name", direction = "up", name = "Deathsurge", target = 1 })
	SZEMain:addCounter("Gremlins", Counter:new{ ctype = "VillainGroup", direction = "up", villainGroup = "CapauDiableDemons", target = 100 })

	self.nextStage = "Stage1"
end
setupStage.shutdown = function(self)
	Message.DebugPrint("Shutting down Stage Setup")
end

stage1 = Stage:new{ name = "Stage1" }
stage1.startup = function(self)
	Message.DebugPrint("Starting Stage 1 for Deathsurge Zone Event")
--	SZEMain.teams["LongbowStage1"]:activate()
	SZEMain.counters["Gremlins"]:init()
--  	The "self." syntax below means that RandomPower is an Object of stage1, so
	self.RandomPower = 0
	math.randomseed( os.time() )

--  Returns true only if self.UI is not yet initialized
	if not self.UI then
		self.UI = UICollections:new
		{
			collections =
			{
				UICollection:new
				{
					-- "Stage1" refers to the UICollection from Zone Events.
					name = "Stage1",
					-- ScriptDisplayName is DisplayName that shows up on top of the UI.
					scriptDisplayName = "Power Overwhelming",
					-- StagedisplayName is the text that shows up inside of the actual UI.
					stageDisplayName = "Smoke on the Water",
					items =
					{
						UIItem:new
						{
							-- Name is the UIItem name from old zone event scripts.
							name = "DeathsurgePower",
							uiType = "BarGold",
							text = "Power is building up in the air...",
							-- CurrentVal needs a function so that it always knows what it's searching for. This function returns the value of a variable defined in the tick below - basically adding up the number of Gremlins defeated with the amount of random Power generated by dice rolls.
							currentVal = function() return stage1.StageProgression() end,
							-- This will make the targeted value equal to the number of Gremlins you would have to kill without getting any Random Power.  While it's not literally accurate in that you can get a higher combo of Random Power and Gremlin power than this, it is the player-facing threshold for the ZE to switch over.
							targetVal = function() return SZEMain.counters["Gremlins"].target end
						}
					}
				}
			}
		}
	end
end
stage1.tick = function(self)
--	Message.DebugPrint("tick of stage 1 for Deathsurge ZE")
--initialize self.time to the current time on the first tick of the stage
	if not self.time then
		self.time = os.time()
	end

--every second, roll dice to check if RandomPower increments
	if os.difftime(os.time(),self.time) >= 1 then
		self.chance = math.random(100)
		Message.DebugPrint("Chance roll: " .. self.chance)
		if self.chance < 6 then
			self.RandomPower = self.RandomPower + 1
			Message.DebugPrint("Random Power: " .. self.RandomPower)
		else
			self.Randompower = self.RandomPower
		end
		self.time = os.time()
	end
	self.StageProgression = function() return SZEMain.counters["Gremlins"].progress + self.RandomPower end

--[[ block comment
	if not self.captionDone and SZEMain.counters["Longbow"].progress >= 3 then
		self.captionDone = true
		 -- Function(player) is going to pass in something for every player within the event. For each player in the event, we're going to send the Message.Caption to the player.
		SZEMain:foreachPlayerInEvent( function(player) Message.Caption(player, "<t=5 caption x=0.3 y=0.2 d=5><scale 1.75><color White><bgcolor DarkRed>This is Commander Bailey. Take down those villains!<t=5 caption x=0.3 y=0.2 d=5><scale 1.75><color White><bgcolor DarkRed>The Family has too many illegal weapons in that casino, we must get them back!")  end )
	end
--]]

	if self.StageProgression() >= 100 then
		Message.DebugPrint("Stage 1 Complete of Deathsurge Zone Event")
		-- "Attention" is a type of floater that you need to specify.
		SZEMain:foreachPlayerInEvent( function(player) Message.Floater(player, "Deathsurge's power electrifies the air!", 0x00ffffff, "Attention")  end )
		self.nextStage = "Stage2"
	end

end
stage1.shutdown = function(self)
	Message.DebugPrint("Shutting down Stage 1 for Deathsurge Event")
	self.captionDone = false
end

